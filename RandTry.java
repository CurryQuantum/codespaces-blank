public class RandTry {

public static void main(String[] args) {
     System.out.println(Math.random()); //1 what does this statement do?
     System.out.println(Math.random()*20); //2 what does this statement do and how is it different from the above statement?
     System.out.println(Math.random()*2); //3 what does this statement do?
     System.out.println((int)Math.random()*5);  //4 what does this statement do?
     System.out.println((int)(Math.random()*20)); //5 what does this statement do?
 }

}